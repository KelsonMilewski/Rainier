				<h2><u><b> Watering </b></u></h2>
				<p>Do not use tap water or mineral water on carnivorous plants. Rainwater or distilled water is ideal. Most carnivorous plants require moist to wet soil in the warmer months and less moisture in winter.</p>

	 			<h2><u><b> Light </b></u></h2>
	 			<p>All carnivorous plants need bright light. They will not produce carnivorous traps unless in a bright windowsill, under artificial light, or outdoors in direct sunlight. </p>

 				<h2><u><b> Soil </b></u></h2>
 				<p>Garden soil is not suitable for carnivorous plants. The preferred media for most are live sphagnum moss, dried long-fiber sphagnum moss or a mix of about three parts peat moss to one part clean sand.</p>

		 		<h2><u><b> Humidity </b></u></h2>
		 		<p>Most carnivorous plants need high humidity. An indoor terrarium or an outdoor garden in humid areas will satisfy this need.</p>
				
