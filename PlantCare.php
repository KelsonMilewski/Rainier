				<h1><i>Forest Terrariums</i></h1>

				<h2><u><b> Watering </b></u></h2>
				<p><b>Open Terrariums</b> - Pour water directly into the soil to make the soil very moist typically once every 7-14 days depending on the season and climate. Don't let open terrariums dry out completely.</p>

			 	<p><b>Closed Terrariums</b> - require little watering, check if the soil is moist. When in doubt, it is always better to not water than to give too much.</p>

			 	<h2><u><b> Light </b></u></h2>
			 	<p>Certain plants need bright, but indirect light. Never place in direct sunlight or outdoors.</p>
				
				<br/>
				<h1><i>Succulent Terrariums</i></h1>

			 	<h2><u><b> Watering </b></u></h2>
			 	<p>Succulent terrariums do require watering. You can spray with a small mist every week. Let the soil dry out between watering.</p>

				<h2><u><b> Light </b></u></h2>
				<p>Succulents like lots of indirect light. Never put in direct sunlight or outdoors. They do well on a window that gets indirect light all day. Too much direct sunlight will cook the plants.</p>
