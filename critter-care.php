				<h1><i>Leopard Geckos</i></h1>

				<h2><u><b> Housing</b></u></h2>
				<p>A 15 to 20GL tank should be a good size for a leopard gecko. Make sure to provide a hiding place both dry and damp. A fish tank could work aswell for leopard geckos. A normal white light incandescent heat bulb could be used to provide a basking spot.</p>

				<h2><u><b>Substrate</b></u></h2>
				<p>For smaller leopard geckos, enclosure carpet or paper towels will work well. For older geckos, you could use enclosure carpet, paper towels, or fine reptile sand that can be used to provide the leopard gecko with a more natural setting.</p>

				<h2><u><b>Temperature</b></u></h2>
				<p> Temperature for a leopard gecko basking spot should 88 to 75 degrees. At night the temperature should be around  70 to 75 degrees. Leopard geckos do not need very humid environments, but if the humidity is under 20% you may notice that your gecko has difficulty shedding.</p>

				<h2><u><b>Food</b></u></h2>
				<p>A shallow dish of water should be given at all times for your leopard gecko. You should feed a variety of crickets, waxworms, mealworms. Insects need to be gut loaded for at least 24 hours before feeding and then covered with a calcium supplement at each feeding. Younger geckos should be fed a few crickets every day, older geckos can skip several days between meals.</p>

