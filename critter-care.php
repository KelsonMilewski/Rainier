				<h1><i>SpringTails</i></h1>

				<h2><u><b>Housing</b></u></h2>
				<p>Using a plastic container with lid ventilation is not required. For the substrate, use a layer of charcoal (horticultural or activated charcoal) with less than an inch of standing water on the bottom. Keep at room temperature and occasionally open the lid for air exchange. </p>

				<h2><u><b>Feeding</b></u></h2>
				<p>Drop in a few grains of rice into your culture. Rice will mold over and will provide a reliable food source. Exchange the old rice with new grains if they have lost interest in the older rice.</p>

