				<h1><i>Isopods</i></h1>

				<h2><u><b>Housing</b></u></h2>
				<p> Plastic boxes with lids are preferred to keep humidity. However, if the top is not vented, the container should be regularly opened. Compressed bedding or mulch works well as a bottom layer in the container. Provide some type of material for the isopods to hide under. They should be kept in a slightly moist enclosure at room temperature.</p>

				<h2><u><b>Watering</b></u></h2>
				<p>Provide a water dish with wet paper towels or cotton balls. Too much standing water will cause them to drown. Use a spray bottle to mist the culture periodically.</p>

				<h2><u><b>Feeding</b></u></h2>
				<p>Feeding your isopods is relatively easy but you need the right food or they will not be strong enough to propagate. You can feed them Lettuce, Carrot , Cucumber, or Squash.</p>

